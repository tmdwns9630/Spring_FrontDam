package kr.or.project.main.model;

public class WalInfo {
	private String damId;
	
	private int waterLevel;
	
	private String mesurDt;

	public String getDamId() {
		return damId;
	}

	public void setDamId(String damId) {
		this.damId = damId;
	}

	public int getWaterLevel() {
		return waterLevel;
	}

	public void setWaterLevel(int waterLevel) {
		this.waterLevel = waterLevel;
	}

	public String getMesurDt() {
		return mesurDt;
	}

	public void setMesurDt(String mesurDt) {
		this.mesurDt = mesurDt;
	}

}
